        private int GetTipoIdentificacionId(string descripcionTipoIdentificacion)
        {
            switch (descripcionTipoIdentificacion?.ToLower())
            {
                case "nit":
                    return 6;
                case "cedula":
                case "cédula ciudadanía":
                case "cc":
                    return 3;
                case "cedula extranjeria":
                case "ce":
                    return 5;
                default:
                    return 3; // Por defecto cedula
            }
        }

        private int GetPaymentFormId(string formaDePago, string sucursal)
        {
            // Handle null or empty payment form
            if (string.IsNullOrWhiteSpace(formaDePago))
            {
                return 1; // Default to cash
            }

            var cleanFormaPago = formaDePago.ToLower();
            
            if (sucursal == "603")
            {
                if (cleanFormaPago.Contains("directo"))
                {
                    return 3;
                }
                else
                {
                    return 1;
                }
            }
            if (cleanFormaPago.Contains("dito") || cleanFormaPago.Contains("cred") || cleanFormaPago.Contains("urbano")
            || cleanFormaPago.Contains("vip")
            || cleanFormaPago.Contains("doble")
            || cleanFormaPago.Contains("micro"))
            {
                return 3;
            }
            else if (cleanFormaPago.Contains("tarjeta"))
            {
                return 2;
            }
            else
            {
                return 1;
            }
        }
