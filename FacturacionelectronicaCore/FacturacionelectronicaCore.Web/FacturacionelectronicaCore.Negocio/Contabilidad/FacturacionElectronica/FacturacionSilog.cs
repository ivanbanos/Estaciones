        private string GetCombustible(string combustible, string estacion)
        {
            // Clean input - remove any numeric prefixes
            if (string.IsNullOrWhiteSpace(combustible))
            {
                combustible = "corriente"; // Default
            }
            
            var cleanCombustible = System.Text.RegularExpressions.Regex.Replace(combustible, @"^\d+", "").Trim().ToLower();
            
            if (cleanCombustible.Contains("corriente"))
            {
                return alegraOptions.Estaciones?.ContainsKey(estacion) == true ?
                     alegraOptions.Estaciones[estacion].Corriente ?? alegraOptions.Corriente :
                     alegraOptions.Corriente;
            }
            else if (cleanCombustible.Contains("diesel") || cleanCombustible.Contains("bioacem") || cleanCombustible.Contains("acpm"))
            {
                return alegraOptions.Estaciones?.ContainsKey(estacion) == true ?
                    alegraOptions.Estaciones[estacion].Acpm ?? alegraOptions.Acpm :
                    alegraOptions.Acpm;
            }
            else if (cleanCombustible.Contains("extra"))
            {
                return alegraOptions.Estaciones?.ContainsKey(estacion) == true ?
                   alegraOptions.Estaciones[estacion].Extra ?? alegraOptions.Extra :
                   alegraOptions.Extra;
            }
            else
            {
                return alegraOptions.Estaciones?.ContainsKey(estacion) == true ?
                    alegraOptions.Estaciones[estacion].Gas ?? alegraOptions.Gas :
                    alegraOptions.Gas;
            }
        }

